def tr(language: str, key: str, **kwargs: Any) -> str:
    lang = "en" if language == "en" else "fa"
    value = TRANSLATIONS.get(lang, TRANSLATIONS["fa"]).get(key)
    if value is None:
        value = TRANSLATIONS["fa"].get(key, key)
    try:
        return value.format(**kwargs)
    except Exception:
        return value
